import fs from 'node:fs';
import path from 'node:path';
import { fileURLToPath, pathToFileURL } from 'node:url';

const SCRIPT_PATH = fileURLToPath(import.meta.url);
const REPO_ROOT = path.resolve(path.dirname(SCRIPT_PATH), '..');
const MARKER_FILE = '.harness-hub-managed';
const PRESERVED_LOCAL_DIRS = new Set(['artifacts']);
const DEFAULT_HOSTS = [
  { id: 'codex', skillsRoot: path.join('.agents', 'skills') },
  { id: 'claude', skillsRoot: path.join('.claude', 'skills') },
];

function toPortable(relativePath) {
  return relativePath.split(path.sep).join('/');
}

function isSkillDir(entry, skillsRoot) {
  return entry.isDirectory() && fs.existsSync(path.join(skillsRoot, entry.name, 'SKILL.md'));
}

function readSkillNames(skillsRoot) {
  if (!fs.existsSync(skillsRoot)) {
    throw new Error(`Missing source skill root: ${skillsRoot}`);
  }

  return fs
    .readdirSync(skillsRoot, { withFileTypes: true })
    .filter((entry) => isSkillDir(entry, skillsRoot))
    .map((entry) => entry.name)
    .sort();
}

function removeManagedEntries(dir) {
  if (!fs.existsSync(dir)) {
    return;
  }

  for (const entry of fs.readdirSync(dir, { withFileTypes: true })) {
    if (PRESERVED_LOCAL_DIRS.has(entry.name)) {
      continue;
    }

    fs.rmSync(path.join(dir, entry.name), { recursive: true, force: true });
  }
}

function pruneIfEmpty(dir) {
  if (fs.existsSync(dir) && fs.readdirSync(dir).length === 0) {
    fs.rmdirSync(dir);
  }
}

function copySkillSource(sourceDir, targetDir) {
  fs.mkdirSync(targetDir, { recursive: true });

  for (const entry of fs.readdirSync(sourceDir, { withFileTypes: true })) {
    if (PRESERVED_LOCAL_DIRS.has(entry.name)) {
      continue;
    }

    const sourcePath = path.join(sourceDir, entry.name);
    const targetPath = path.join(targetDir, entry.name);
    if (entry.isDirectory()) {
      copySkillSource(sourcePath, targetPath);
    } else if (entry.isFile()) {
      fs.copyFileSync(sourcePath, targetPath);
    }
  }
}

function writeMarker(targetDir, skillName, hostId) {
  fs.writeFileSync(
    path.join(targetDir, MARKER_FILE),
    [
      'Generated by scripts/sync-agent-skills.mjs.',
      `Host: ${hostId}`,
      `Source: skills/${skillName}`,
      'Do not edit this copy; edit the source skill instead.',
      '',
    ].join('\n'),
  );
}

function normalizeHosts(hostIds) {
  if (!hostIds || hostIds.length === 0) {
    return DEFAULT_HOSTS;
  }

  const knownHosts = new Map(DEFAULT_HOSTS.map((host) => [host.id, host]));
  return hostIds.map((hostId) => {
    const host = knownHosts.get(hostId);
    if (!host) {
      throw new Error(`Unknown host: ${hostId}. Expected one of: ${DEFAULT_HOSTS.map((entry) => entry.id).join(', ')}`);
    }
    return host;
  });
}

function planHostSkillSync(root, host, sourceSkillNames) {
  const hostSkillsRoot = path.join(root, host.skillsRoot);
  const sourceSkillSet = new Set(sourceSkillNames);
  const existingTargetEntries = fs.existsSync(hostSkillsRoot)
    ? fs.readdirSync(hostSkillsRoot, { withFileTypes: true }).filter((entry) => entry.isDirectory())
    : [];

  const staleManaged = existingTargetEntries
    .filter((entry) => !sourceSkillSet.has(entry.name))
    .map((entry) => entry.name)
    .sort();

  return {
    id: host.id,
    skillsRoot: hostSkillsRoot,
    relativeSkillsRoot: host.skillsRoot,
    staleManaged,
  };
}

export function planAgentSkillSync(options = {}) {
  const root = path.resolve(options.root ?? REPO_ROOT);
  const skillsRoot = path.join(root, 'skills');
  const sourceSkillNames = readSkillNames(skillsRoot);
  const hosts = normalizeHosts(options.hosts).map((host) => planHostSkillSync(root, host, sourceSkillNames));

  return {
    root,
    skillsRoot,
    sourceSkillNames,
    hosts,
    blocked: [],
  };
}

export function syncAgentSkills(options = {}) {
  const dryRun = options.dryRun === true;
  const plan = planAgentSkillSync(options);

  for (const host of plan.hosts) {
    if (!dryRun) {
      fs.mkdirSync(host.skillsRoot, { recursive: true });
    }

    for (const skillName of host.staleManaged) {
      const targetDir = path.join(host.skillsRoot, skillName);
      if (!dryRun) {
        removeManagedEntries(targetDir);
        pruneIfEmpty(targetDir);
      }
    }

    for (const skillName of plan.sourceSkillNames) {
      const sourceDir = path.join(plan.skillsRoot, skillName);
      const targetDir = path.join(host.skillsRoot, skillName);
      if (!dryRun) {
        removeManagedEntries(targetDir);
        copySkillSource(sourceDir, targetDir);
        writeMarker(targetDir, skillName, host.id);
      }
    }
  }

  return {
    ...plan,
    dryRun,
    copied: plan.sourceSkillNames.length,
    staleRemoved: plan.hosts.reduce((count, host) => count + host.staleManaged.length, 0),
  };
}

function parseArgs(argv) {
  const options = { dryRun: false, root: undefined, hosts: undefined };

  for (let index = 0; index < argv.length; index += 1) {
    const arg = argv[index];
    if (arg === '--dry-run') {
      options.dryRun = true;
      continue;
    }
    if (arg === '--host') {
      const value = argv[index + 1];
      if (!value) {
        throw new Error('--host requires a value');
      }
      options.hosts = [...(options.hosts ?? []), value];
      index += 1;
      continue;
    }
    if (arg === '--root') {
      const value = argv[index + 1];
      if (!value) {
        throw new Error('--root requires a value');
      }
      options.root = value;
      index += 1;
      continue;
    }
    throw new Error(`Unknown option: ${arg}`);
  }

  return options;
}

function printSummary(result) {
  const action = result.dryRun ? 'Would sync' : 'Synced';
  for (const host of result.hosts) {
    console.log(`${action} ${result.copied} skills into ${toPortable(path.relative(result.root, host.skillsRoot))}/`);
    if (host.staleManaged.length > 0) {
      console.log(`${result.dryRun ? 'Would remove' : 'Removed'} ${host.staleManaged.length} stale skill directories from ${host.id}.`);
    }
  }
  console.log(
    'Note: this command writes project-local host mirrors only; it does not install user-level/global skills for direct slash invocation.',
  );
}

if (import.meta.url === pathToFileURL(process.argv[1]).href) {
  try {
    const result = syncAgentSkills(parseArgs(process.argv.slice(2)));
    printSummary(result);
  } catch (error) {
    console.error(error instanceof Error ? error.message : String(error));
    process.exit(1);
  }
}
