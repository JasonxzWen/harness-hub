import fs from 'node:fs';
import path from 'node:path';
import { fileURLToPath, pathToFileURL } from 'node:url';

const SCRIPT_PATH = fileURLToPath(import.meta.url);
const REPO_ROOT = path.resolve(path.dirname(SCRIPT_PATH), '..');
const MARKER_FILE = '.skill-hub-managed';
const PRESERVED_LOCAL_DIRS = new Set(['artifacts']);

function toPortable(relativePath) {
  return relativePath.split(path.sep).join('/');
}

function isSkillDir(entry, skillsRoot) {
  return entry.isDirectory() && fs.existsSync(path.join(skillsRoot, entry.name, 'SKILL.md'));
}

function readSkillNames(skillsRoot) {
  if (!fs.existsSync(skillsRoot)) {
    throw new Error(`Missing source skill root: ${skillsRoot}`);
  }

  return fs
    .readdirSync(skillsRoot, { withFileTypes: true })
    .filter((entry) => isSkillDir(entry, skillsRoot))
    .map((entry) => entry.name)
    .sort();
}

function meaningfulEntries(dir) {
  if (!fs.existsSync(dir)) {
    return [];
  }

  return fs
    .readdirSync(dir, { withFileTypes: true })
    .filter((entry) => entry.name !== MARKER_FILE && !PRESERVED_LOCAL_DIRS.has(entry.name))
    .map((entry) => entry.name)
    .sort();
}

function isManagedCodexSkill(dir) {
  return fs.existsSync(path.join(dir, MARKER_FILE));
}

function removeManagedEntries(dir) {
  if (!fs.existsSync(dir)) {
    return;
  }

  for (const entry of fs.readdirSync(dir, { withFileTypes: true })) {
    if (PRESERVED_LOCAL_DIRS.has(entry.name)) {
      continue;
    }

    fs.rmSync(path.join(dir, entry.name), { recursive: true, force: true });
  }
}

function pruneIfEmpty(dir) {
  if (fs.existsSync(dir) && fs.readdirSync(dir).length === 0) {
    fs.rmdirSync(dir);
  }
}

function copySkillSource(sourceDir, targetDir) {
  fs.mkdirSync(targetDir, { recursive: true });

  for (const entry of fs.readdirSync(sourceDir, { withFileTypes: true })) {
    if (PRESERVED_LOCAL_DIRS.has(entry.name)) {
      continue;
    }

    const sourcePath = path.join(sourceDir, entry.name);
    const targetPath = path.join(targetDir, entry.name);
    if (entry.isDirectory()) {
      copySkillSource(sourcePath, targetPath);
    } else if (entry.isFile()) {
      fs.copyFileSync(sourcePath, targetPath);
    }
  }
}

function writeMarker(targetDir, skillName) {
  fs.writeFileSync(
    path.join(targetDir, MARKER_FILE),
    [
      'Generated by scripts/sync-codex-skills.mjs.',
      `Source: skills/${skillName}`,
      'Do not edit this copy; edit the source skill instead.',
      '',
    ].join('\n'),
  );
}

export function planCodexSkillSync(options = {}) {
  const root = path.resolve(options.root ?? REPO_ROOT);
  const skillsRoot = path.join(root, 'skills');
  const codexSkillsRoot = path.join(root, '.codex', 'skills');
  const sourceSkillNames = readSkillNames(skillsRoot);
  const sourceSkillSet = new Set(sourceSkillNames);
  const existingTargetEntries = fs.existsSync(codexSkillsRoot)
    ? fs.readdirSync(codexSkillsRoot, { withFileTypes: true }).filter((entry) => entry.isDirectory())
    : [];

  const blocked = existingTargetEntries
    .filter((entry) => !sourceSkillSet.has(entry.name))
    .filter((entry) => !isManagedCodexSkill(path.join(codexSkillsRoot, entry.name)))
    .map((entry) => `.codex/skills/${entry.name}`);

  for (const skillName of sourceSkillNames) {
    const targetDir = path.join(codexSkillsRoot, skillName);
    if (!fs.existsSync(targetDir) || isManagedCodexSkill(targetDir)) {
      continue;
    }

    const entries = meaningfulEntries(targetDir);
    if (entries.length > 0) {
      blocked.push(`.codex/skills/${skillName}`);
    }
  }

  const staleManaged = existingTargetEntries
    .filter((entry) => !sourceSkillSet.has(entry.name))
    .filter((entry) => isManagedCodexSkill(path.join(codexSkillsRoot, entry.name)))
    .map((entry) => entry.name)
    .sort();

  return {
    root,
    skillsRoot,
    codexSkillsRoot,
    sourceSkillNames,
    staleManaged,
    blocked: blocked.sort(),
  };
}

export function syncCodexSkills(options = {}) {
  const dryRun = options.dryRun === true;
  const plan = planCodexSkillSync(options);

  if (plan.blocked.length > 0) {
    throw new Error(
      [
        'Refusing to overwrite unmanaged Codex skill directories:',
        ...plan.blocked.map((entry) => `- ${entry}`),
        'Remove them manually or add a .skill-hub-managed marker if they are generated copies.',
      ].join('\n'),
    );
  }

  if (!dryRun) {
    fs.mkdirSync(plan.codexSkillsRoot, { recursive: true });
  }

  for (const skillName of plan.staleManaged) {
    const targetDir = path.join(plan.codexSkillsRoot, skillName);
    if (!dryRun) {
      removeManagedEntries(targetDir);
      pruneIfEmpty(targetDir);
    }
  }

  for (const skillName of plan.sourceSkillNames) {
    const sourceDir = path.join(plan.skillsRoot, skillName);
    const targetDir = path.join(plan.codexSkillsRoot, skillName);
    if (!dryRun) {
      removeManagedEntries(targetDir);
      copySkillSource(sourceDir, targetDir);
      writeMarker(targetDir, skillName);
    }
  }

  return {
    ...plan,
    dryRun,
    copied: plan.sourceSkillNames.length,
    staleRemoved: plan.staleManaged.length,
  };
}

function parseArgs(argv) {
  const options = { dryRun: false, root: undefined };

  for (let index = 0; index < argv.length; index += 1) {
    const arg = argv[index];
    if (arg === '--dry-run') {
      options.dryRun = true;
      continue;
    }
    if (arg === '--root') {
      const value = argv[index + 1];
      if (!value) {
        throw new Error('--root requires a value');
      }
      options.root = value;
      index += 1;
      continue;
    }
    throw new Error(`Unknown option: ${arg}`);
  }

  return options;
}

function printSummary(result) {
  const action = result.dryRun ? 'Would sync' : 'Synced';
  console.log(`${action} ${result.copied} skills into ${toPortable(path.relative(result.root, result.codexSkillsRoot))}/`);
  if (result.staleRemoved > 0) {
    console.log(`${result.dryRun ? 'Would remove' : 'Removed'} ${result.staleRemoved} stale managed skill directories.`);
  }
}

if (import.meta.url === pathToFileURL(process.argv[1]).href) {
  try {
    const result = syncCodexSkills(parseArgs(process.argv.slice(2)));
    printSummary(result);
  } catch (error) {
    console.error(error instanceof Error ? error.message : String(error));
    process.exit(1);
  }
}
